# ── JSON-RPC dispatch ───────────────────────────────────────────────


class RpcError(Exception):
    def __init__(self, code: int, message: str, data: Any = None):
        super().__init__(message)
        self.code = code
        self.message = message
        self.data = data


def _err(code: int, message: str, data: Any = None) -> dict:
    err = {"code": code, "message": message}
    if data is not None:
        err["data"] = data
    return err


def _handle_initialize(_params: dict) -> dict:
    threading.Thread(target=_start_dashboard, daemon=True, name="neo-dashboard-init").start()
    return {
        "protocolVersion": PROTOCOL_VERSION,
        "capabilities": {"tools": {"listChanged": False}},
        "serverInfo": {"name": SERVER_NAME, "version": SERVER_VERSION},
    }


def _handle_tools_list(_params: dict) -> dict:
    return {
        "tools": [
            {
                "name": name,
                "description": spec["description"],
                "inputSchema": spec["schema"],
            }
            for name, spec in TOOLS.items()
        ]
    }


def _handle_tools_call(params: dict) -> dict:
    name = params.get("name")
    if not name or name not in TOOLS:
        raise RpcError(-32602, f"unknown tool: {name}")
    args = params.get("arguments") or {}
    if not isinstance(args, dict):
        raise RpcError(-32602, "arguments must be an object")
    try:
        result = TOOLS[name]["fn"](args)
        return _text_result(result)
    except Exception as exc:
        traceback.print_exc(file=sys.stderr)
        return {
            "content": [{"type": "text", "text": f"tool error: {exc}"}],
            "isError": True,
        }


METHODS: dict[str, Callable[[dict], dict]] = {
    "initialize": _handle_initialize,
    "tools/list": _handle_tools_list,
    "tools/call": _handle_tools_call,
}


def _dispatch(msg: dict) -> dict | None:
    method = msg.get("method")
    msg_id = msg.get("id")
    params = msg.get("params") or {}

    if method is None:
        return {"jsonrpc": "2.0", "id": msg_id, "error": _err(-32600, "missing method")}

    if method.startswith("notifications/") or msg_id is None:
        return None

    handler = METHODS.get(method)
    if handler is None:
        return {"jsonrpc": "2.0", "id": msg_id, "error": _err(-32601, f"method not found: {method}")}

    try:
        result = handler(params)
        return {"jsonrpc": "2.0", "id": msg_id, "result": result}
    except RpcError as exc:
        return {"jsonrpc": "2.0", "id": msg_id, "error": _err(exc.code, exc.message, exc.data)}
    except Exception as exc:
        traceback.print_exc(file=sys.stderr)
        return {"jsonrpc": "2.0", "id": msg_id, "error": _err(-32603, f"internal error: {exc}")}


def _send(response: dict) -> None:
    sys.stdout.write(json.dumps(response, default=str) + "\n")
    sys.stdout.flush()


def serve() -> None:
    for raw in sys.stdin:
        line = raw.strip()
        if not line:
            continue
        try:
            msg = json.loads(line)
        except json.JSONDecodeError:
            _send({"jsonrpc": "2.0", "id": None, "error": _err(-32700, "parse error")})
            continue
        if not isinstance(msg, dict):
            continue
        response = _dispatch(msg)
        if response is not None:
            _send(response)


def main() -> None:
    serve()


if __name__ == "__main__":
    main()
